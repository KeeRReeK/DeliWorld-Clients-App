import SwiftUI

// MARK: - Font Extensions
extension Font {
    static func leagueSpartanMedium(size: CGFloat) -> Font {
        return .custom("LeagueSpartan-Medium", size: size)
    }
    
    static func leagueSpartanRegular(size: CGFloat) -> Font {
        return .custom("LeagueSpartan-Regular", size: size)
    }
    
    static func leagueSpartanLight(size: CGFloat) -> Font {
        return .custom("LeagueSpartan-Light", size: size)
    }
    
    static func leagueSpartanBold(size: CGFloat) -> Font {
        return .custom("LeagueSpartan-Bold", size: size)
    }
    
    static func leagueSpartanSemiBold(size: CGFloat) -> Font {
        return .custom("LeagueSpartan-SemiBold", size: size)
    }
    
    static func interRegularBlack(size: CGFloat) -> Font {
        return .custom("Inter-Regular_Black", size: size)
    }
    
    
    
}
